document.querySelector("#sort").addEventListener("change",handleName)

function handleName(){
var selected=document.querySelector('#sort').value

if(selected=="asc"){

    data.sort(function(a,b){
        if(a.p_name>b.p_name){
            return 1
        }
        if(a.p_name<b.p_name){
            return -1
        }
        return 0
    })

    displayData(data);

}
if(selected=="dsc"){

    data.sort(function(a,b){
        if(a.p_name>b.p_name){
            return -1
        }
        if(a.p_name<b.p_name){
            return 1
        }
        return 0
    })
 
    displayData(data);
   

}
   
}

document.querySelector("#sort").addEventListener("change",handlePrice)

function handlePrice(){

    var price=document.querySelector("#sort").value

    // console.log(price);
    if(price=="HTL"){

        data.sort(function (a,b){

            return  Number(b.p_dprice)-Number(a.p_dprice)
           
        })
        displayData(data);
        
    }
    if(price=="LTH"){

        data.sort(function (a,b){

            let diff=Number(a.p_dprice)-Number(b.p_dprice)
            return diff
           
        })
        displayData(data);

    }
    

   

}



